public class LongiCircu {
	
	public static void main (String[] args) {
//declaramos las variables constantes//
		final double pi = 3.14;
		final double R = 7.125;
//declaramos la variable "L" que sera la encargada de realizar los calculos//
		double L;
//realizamos los calculos//
	L= 2* pi * R;
//imprimimos en pantalla//
		System.out.println("La longitud de un circulo de radio es = "+L);
		
	}
}

